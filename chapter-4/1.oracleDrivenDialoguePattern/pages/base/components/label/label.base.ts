export default class LabelBase {

    /**
     * Mapeamento base do alerta de sucesso
     * @returns - Retorna o elemento do alerta de sucesso
     */
    public lblAlertSuccess() { return `.alert-success.alert` }

    /**
     * Mapeamento base do título da página
     * @returns - Retorna o elemento do título da página
     */
    public lblTitle() { return `h2.title` }
}